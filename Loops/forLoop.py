# Iterate a list using for loop
fruits = ["mango", "apple", "orange"]
for value in fruits:
    print(value)

#iterate range function using for loop
for value in range(1,5):
    print(value)

#iterate the range function using step size
for value in range(-2,8, 2):
    print(value)

# iterate the string using for loop
name = "Sylar009"
for value in name:
    print(value)

