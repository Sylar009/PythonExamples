# Set treats each character as different element
firstSet = set('abc')
print(firstSet)

# In Set there is no duplicate values and they are unordered
dupSet = set("abcadecba")
print(dupSet)

# Lets convert the list into set and they are unordered
secondSet = set(["apple", "orange", "banana", "orange"])
print(secondSet)

# Output of the above print is unordered
